    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $product = $request->all();
        if ($request->file("gallery")){
            $productFile = $request->file("gallery");
            $productFileName = time().$productFile->getClientOriginalName();
            $productFileName = str_replace(" ","",$productFileName);
            $product["gallery"] = $productFileName;
            $productFile->move("uploads",$productFileName);
            Upload::create($product);
            echo "File save successfully";
        }else{
            echo "Select files to upload";
        }
    }

